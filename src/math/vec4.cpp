    /*
     * Check if vector value is equal to other vector's value
     */
    template<typename T>
    deng_bool_t vec4<T>::operator==(const vec4<T> &vec) const { 
        return (deng_bool_t) (                
            first == vec.first && 
            second == vec.second && 
            third == vec.third && 
            fourth == vec.fourth
        ); 
    }


