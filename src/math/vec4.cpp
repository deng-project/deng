    /*
     * Find the dot product between current vector instance and the
     * vector given as an argument
     */
    template<typename T>
    T vec4<T>::operator*(const vec4<T> &vec) const {
        if(std::is_floating_point<T>::value || std::is_integral<T>::value) {
            T out = (
                first * vec.first +
                second * vec.second +
                third * vec.third +
                fourth * vec.fourth
            );

            return out; 
        }

        else return T{};
    }


